package collection;


public class Student1 {

	int id;
	int age;
	String name;
	public Student1(int id, int age, String name) {
		super();
		this.id = id;
		this.age = age;
		this.name = name;
	}
	@Override
	public String toString() {
		return "Student1 [id=" + id + ", age=" + age + ", name=" + name + "]";
	}	
	
}


	
